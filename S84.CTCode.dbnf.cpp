template<char character>
class CharacterParser
{
public:
    static s84::ctcode::dbnf::String* Parse(s84::ctcode::dbnf::LengthString& index)
    {
        s84::ctcode::dbnf::LengthString data = {index.data, 1};

        if((index.length > 0) && ((*(index.data)) == character))
        {
            ++(index.data);
            --(index.length);
            s84::ctcode::dbnf::String* string_node = new s84::ctcode::dbnf::String(data);
            return string_node;
        }
        else
        {
            return NULL;
        }
    }
};

template<typename Literal>
class StringParser
{
public:
    static bool MatchCharacter(char value, s84::ctcode::dbnf::LengthString& index)
    {
        if((index.length > 0) && ((*(index.data)) == value))
        {
            ++(index.data);
            --(index.length);
            return true;
        }
        else
        {
            return false;
        }
    }

    static s84::ctcode::dbnf::String* Parse(s84::ctcode::dbnf::LengthString& index)
    {
        s84::ctcode::dbnf::LengthString start = index;
        const char* literal_index = Literal::GetLiteral();
        s84::ctcode::dbnf::LengthString data = {index.data, strlen(literal_index)};
        
        while((*literal_index) && (start.length > 0) && MatchCharacter(*literal_index, start))
        {
            ++literal_index;
        }
        
        if((*literal_index) == 0)
        {
            index = start;
            s84::ctcode::dbnf::String* string_node = new s84::ctcode::dbnf::String(data);
            return string_node;
        }
        else
        {
            return NULL;
        }
    }

};

