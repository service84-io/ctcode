static bool Match(std::list<s84::ctcode::dbnf::Node*>& nodes, s84::ctcode::dbnf::Node* node)
{
    if(node)
    {
        nodes.push_back(node);
        return true;
    }
    else
    {
        return false;
    }
}

static bool Optional(std::list<s84::ctcode::dbnf::Node*>& nodes, s84::ctcode::dbnf::Node* node)
{
    Match(nodes, node);
    return true;
}

template<typename Reference, typename Parser>
static bool Match(std::list<s84::ctcode::dbnf::Node*>& nodes, Reference*& reference, Parser* node)
{
    reference = node;
    return Match(nodes, node);
}

template<typename Reference, typename Parser>
static bool Optional(std::list<s84::ctcode::dbnf::Node*>& nodes, Reference*& reference, Parser* node)
{
    Match<Reference, Parser>(nodes, reference, node);
    return true;
}

static bool Reset(s84::ctcode::dbnf::LengthString value, s84::ctcode::dbnf::LengthString& index)
{
    index = value;
    return false;
}

static bool ClearNodes(std::list<s84::ctcode::dbnf::Node*>& nodes)
{
    while(!nodes.empty())
    {
        s84::ctcode::dbnf::Node* node = nodes.front();
        nodes.pop_front();
        delete node;
    }

    return true;
}

template<typename Parser, int minimum, int maximum>
class RangeParser : public s84::ctcode::dbnf::List<Parser>
{
public:
    RangeParser()
    {
    }
    virtual ~RangeParser()
    {
    }

    static RangeParser<Parser, minimum, maximum>* Parse(s84::ctcode::dbnf::LengthString& index)
    {
        s84::ctcode::dbnf::LengthString start = index;
        s84::ctcode::dbnf::LengthString local_start = index;
        std::list<s84::ctcode::dbnf::Node*> children;
        RangeParser<Parser, minimum, maximum>* range_parser = NULL;
        std::list<Parser*> list;
        Parser* node = NULL;
        int count = 0;

        while(((count < maximum) && (node = Parser::Parse(index))) || s84::ctcode::dbnf::Reset(local_start, index))
        {
            local_start = index;
            ++count;
            children.push_back(node);
            list.push_back(node);
        }

        if((count > minimum) || (s84::ctcode::dbnf::ClearNodes(children) && s84::ctcode::dbnf::Reset(start, index)))
        {
            range_parser = new RangeParser<Parser, minimum, maximum>();
            range_parser->SetChildren(children);
            range_parser->list_ = list;
        }

        return range_parser;
    }
};

